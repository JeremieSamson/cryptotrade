<?php

namespace AppBundle\Entity\Alert;

use Doctrine\ORM\Mapping as ORM;

/**
 * TwitterAlert
 *
 * @ORM\Table(name="alert_twitter")
 * @ORM\Entity(repositoryClass="AppBundle\Repository\Alert\TwitterAlertRepository")
 */
class TwitterAlert extends Alert
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="author", type="string", length=255)
     */
    private $author;

    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set author
     *
     * @param string $author
     *
     * @return TwitterAlert
     */
    public function setAuthor($author)
    {
        $this->author = $author;

        return $this;
    }

    /**
     * Get author
     *
     * @return string
     */
    public function getAuthor()
    {
        return $this->author;
    }
}

